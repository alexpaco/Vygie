			<p>
				Vygie est une application de lancement d’alerte de certaines épidémies touchant principalement les jeunes enfants en milieu scolaire. Le concept repose sur le signalement d’un début d’épidémie (poux, varicelle, gastro…) par le corps pédagogique. 
L’objectif est double, d’une part anticiper l’épidémie en identifiant les facteurs de risques en collectivité afin de permettre aux établissements scolaires d’augmenter leur vigilance. Et d’autre part, partager et transmettre cette information (sous forme d’alertes) entre les établissements scolaires et les acteurs de l’enseignement primaire à échelle locale pour prévenir des risques épidémiologiques.

Grâce au bouton Vygie, le lanceur d’alerte prévient immédiatement l’ensemble de la communauté, il suffit de cliquer pour transmettre l’information.
